package AccessSpecifier;

public class As1 {
	public void m1()
	{
		System.out.println("public method");
	}
	private void m2()
	{
		System.out.println("private method");
	}
	void m3()
	{
		System.out.println("default method");
	}
	protected void m4()
	{
		System.out.println("protected method");
	}
}

