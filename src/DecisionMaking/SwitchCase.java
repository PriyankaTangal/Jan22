package DecisionMaking;

public class SwitchCase {
	public static void main(String[] args) {
		String input="five";
		switch(input)
		{
		case "one": System.out.println("i am 1"); break;
		case "two": System.out.println("i am 2"); break;
		case "three": System.out.println("i am 3"); break;
		case "four": System.out.println("i am 4"); break;
		case "five": System.out.println("i am 5"); break;
		case "six": System.out.println("i am 6"); break;
		default: System.out.println("i am default"); break;
		}
	}
}
